    public void combo_id_capres(){
         id_capres.removeAllItems();
        
        try{
            Statement perintah = (Statement) koneksi.GetConnection().createStatement();
            String sql = "Select * from capres";
            ResultSet res = perintah.executeQuery(sql);
            while(res.next()){
                id_capres.addItem(res.getString("id_capres"));
            }
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null,"Error"+err);
     
    }
    }

