    private void id_capresItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_id_capresItemStateChanged
           try{
            Statement perintah = (Statement) koneksi.GetConnection().createStatement();
            String sql ="Select * from capres where id_capres = '"+id_capres.getSelectedItem()+"'";
            ResultSet res = perintah.executeQuery(sql);
            while(res.next()){
                nama_capres.setText(res.getString("nama_capres"));
                no_urut.setText(res.getString("no_urut"));
            }
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null,"Error"+err);
        }
    }//GEN-LAST:event_id_capresItemStateChanged

